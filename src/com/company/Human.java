package com.company;

public class Human {
    String name;
    int salary;
    int waight;

    public Human(String name, int salary, int waight) {
        this.name = name;
        this.salary = salary;
        this.waight = waight;
    }

    public String getName() {
        return name;
    }

    public int getSalary() {
        return salary;
    }

    public int getWaight() {
        return waight;
    }

}
